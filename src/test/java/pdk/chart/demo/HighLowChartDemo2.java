    private static Chart createChart(OHLCDataset dataset) {
        Chart chart = ChartFactory.createHighLowChart("HighLowChartDemo2", "Time", "Value", dataset, true);
        XYPlot plot = (XYPlot) chart.getPlot();
        DateAxis axis = (DateAxis) plot.getDomainAxis();
        axis.setTickMarkPosition(DateTickMarkPosition.MIDDLE);
        NumberAxis yAxis = (NumberAxis) plot.getRangeAxis();
        yAxis.setNumberFormatOverride(new DecimalFormat("$0.00"));
        XYDataset dataset2 = MovingAverage.createMovingAverage(dataset, "-MAVG", 259200000L, 0L);
        plot.setDataset(1, dataset2);
        plot.setRenderer(1, new StandardXYItemRenderer());
        return chart;
    }

    public static JPanel createDemoPanel() {
        Chart chart = createChart(createDataset());
        return new ChartPanel(chart);
    }

