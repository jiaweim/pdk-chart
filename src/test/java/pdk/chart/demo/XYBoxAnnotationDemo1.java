        annotation = new XYBoxAnnotation((double)(new Day(1, 2, 2004)).getMiddleMillisecond(), (double)0.0F, (double)(new Day(29, 2, 2004)).getMiddleMillisecond(), (double)50000.0F, new BasicStroke(0.0F), transpRed, transpRed);
