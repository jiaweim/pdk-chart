    private static Chart createCombinedChart() {
        TimeSeriesCollection dataset0 = new TimeSeriesCollection();
        TimeSeries eur = createEURTimeSeries();
        dataset0.addSeries(eur);
        TimeSeriesCollection dataset1 = new TimeSeriesCollection();
        TimeSeries mav = MovingAverage.createMovingAverage(eur, "EUR/GBP (30 Day MA)", 30, 30);
        dataset1.addSeries(eur);
        dataset1.addSeries(mav);
        TimeSeriesCollection dataset2 = new TimeSeriesCollection();
        dataset2.addSeries(eur);
        Chart chart = null;
        NumberAxis valueAxis = new NumberAxis("Value");
        valueAxis.setAutoRangeIncludesZero(false);
        CombinedRangeXYPlot parent = new CombinedRangeXYPlot(valueAxis);
        XYPlot subplot1 = new XYPlot(dataset0, new DateAxis("Date 1"), (ValueAxis) null, new StandardXYItemRenderer());
        parent.add(subplot1, 1);
        XYPlot subplot2 = new XYPlot(dataset1, new DateAxis("Date 2"), (ValueAxis) null, new StandardXYItemRenderer());
        parent.add(subplot2, 1);
        XYPlot subplot3 = new XYPlot(dataset2, new DateAxis("Date 3"), (ValueAxis) null, new XYBarRenderer(0.2));
        parent.add(subplot3, 1);
        chart = new Chart("Demo Chart", Chart.DEFAULT_TITLE_FONT, parent, true);
        TextTitle subtitle = new TextTitle("This is a subtitle", new Font("SansSerif", 1, 12));
        chart.addSubtitle(subtitle);
        chart.setBackgroundPaint(new GradientPaint(0.0F, 0.0F, Color.WHITE, 0.0F, 1000.0F, Color.BLUE));
        JChartUtils.applyCurrentTheme(chart);
        return chart;
    }

