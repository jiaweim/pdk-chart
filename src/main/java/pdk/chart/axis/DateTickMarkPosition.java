package pdk.chart.axis;

/**
 * Used to indicate the required position of tick marks on a date axis relative
 * to the underlying time period.
 */
public enum DateTickMarkPosition {

    /**
     * Start of period.
     */
    START,

    /**
     * Middle of period.
     */
    MIDDLE,

    /**
     * End of period.
     */
    END

}
