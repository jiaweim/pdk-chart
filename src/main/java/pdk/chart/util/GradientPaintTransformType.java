package pdk.chart.util;

/**
 * Represents a type of transform for a {@code GradientPaint}.
 */
public enum GradientPaintTransformType {

    /**
     * Vertical.
     */
    VERTICAL,

    /**
     * Horizontal.
     */
    HORIZONTAL,

    /**
     * Center/vertical.
     */
    CENTER_VERTICAL,

    /**
     * Center/horizontal.
     */
    CENTER_HORIZONTAL

}
