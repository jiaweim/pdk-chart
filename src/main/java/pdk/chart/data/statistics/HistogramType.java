package pdk.chart.data.statistics;

/**
 * Enum to represent the histogram type.
 */
public enum HistogramType {

    /**
     * Frequency histogram.
     */
    FREQUENCY,

    /**
     * Relative frequency.
     */
    RELATIVE_FREQUENCY,

    /**
     * Scale area to one.
     */
    SCALE_AREA_TO_1

}
