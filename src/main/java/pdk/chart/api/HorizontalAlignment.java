package pdk.chart.api;

/**
 * An enumeration of the horizontal alignment types ({@code LEFT},
 * {@code RIGHT} and {@code CENTER}).
 */
public enum HorizontalAlignment {

    /**
     * Left alignment.
     */
    LEFT,

    /**
     * Right alignment.
     */
    RIGHT,

    /**
     * Center alignment.
     */
    CENTER

}
